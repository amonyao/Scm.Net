<template>
	<el-container>
		<el-header>
			<div class="left-panel">
				<el-button icon="el-icon-plus" type="primary" @click="open_dialog()" />
				<el-divider direction="vertical"></el-divider>
				<sc-file-import :api-obj="uploadApi" :template-url="templateUrl" title="导入"></sc-file-import>
				<el-divider direction="vertical"></el-divider>
				<el-button-group>
					<el-tooltip content="启用">
						<el-button type="primary" icon="el-icon-circle-check" plain :disabled="selection.length == 0"
							@click="status_list(1)"></el-button>
					</el-tooltip>
					<el-tooltip content="停用">
						<el-button type="primary" icon="el-icon-circle-close" plain :disabled="selection.length == 0"
							@click="status_list(2)"></el-button>
					</el-tooltip>
					<el-tooltip content="删除">
						<el-button type="danger" icon="el-icon-delete" plain :disabled="selection.length == 0"
							@click="delete_list"></el-button>
					</el-tooltip>
				</el-button-group>
			</div>
			<div class="right-panel">
				<div class="right-panel-search">
					<el-input v-model="param.key" clearable placeholder="关键字" v-on:input="usernameInput()"
						v-on:keydown="userNameKeydown($event)" v-on:keyup="userNameKeyup($event)"
						v-on:paste="usernamePaste()" @blur="blurUsername()" />
					<el-button icon="el-icon-search" type="primary" @click="search" />
				</div>
			</div>
		</el-header>
		<el-main class="nopadding">
			<scTable ref="table" :api-obj="apiObj" :column="column" row-key="id" @menu-handle="menuHandle"
				@selection-change="selectionChange">
				<!-- 固定列-选择列 -->
				<el-table-column align="center" fixed type="selection" width="60" />
				<el-table-column label="#" type="index" width="50" />
				<el-table-column label="操作" align="center" fixed="right" width="140">
					<template #default="scope">
						<el-button text type="primary" size="small" @click="open_dialog(scope.row)">
							编辑
						</el-button>
						<el-divider direction="vertical" />
						<el-popconfirm title="确定删除吗？" @confirm="delete_item(scope.row, scope.$index)">
							<template #reference>
								<el-button text type="primary" size="small">删除</el-button>
							</template>
						</el-popconfirm>
					</template>
				</el-table-column>
				<template #row_status="scope">
					<el-tooltip :content="scope.row.row_status ? '正常' : '停用'" placement="right">
						<el-switch v-model="scope.row.row_status" :active-value="1" :inactive-value="2"
							@change="status_item($event, scope.row)">
						</el-switch>
					</el-tooltip>
				</template>
			</scTable>
		</el-main>
	</el-container>
	<edit ref="edit" @complete="complete" />
</template>
<script>
import { defineAsyncComponent } from "vue";
import config from "@/config"

export default {
	components: {
		edit: defineAsyncComponent(() => import("./edit"))
	},
	data() {
		return {
			apiObj: this.$API.samplesdemo.page,
			uploadApi: this.$API.samplesdemo.upload,
			templateUrl: `${config.SERVER_URL}/upload/templates/示例模板.xlsx`,
			param: {
				option_id: '0',
				row_status: '1',
				create_time: '',
				key: ''
			},
			option_list: [{ key: '111', value: '112' }],
			statusList: [{ key: '111', value: '112' }],
			selection: [],
			column: [
				{ label: "id", prop: "id", hide: true },
				{ prop: 'codes', label: '系统代码', width: 100 },
				{ prop: 'codec', label: '客户编码', width: 100 },
				{ prop: 'names', label: '系统简称', width: 100 },
				{ prop: 'namec', label: '客户全称', width: 100 },
				{ prop: 'phone', label: '电话', width: 100 },
				{ prop: 'remark', label: '备注', width: 100 },
				{ prop: 'row_status', label: '数据状态', width: 100 },
				{ prop: 'update_time', label: '更新时间', width: 160 },
				{ prop: 'update_names', label: '更新人员', width: 100 },
				{ prop: 'create_time', label: '创建时间', width: 160 },
				{ prop: 'create_names', label: '创建人员', width: 100 },
			],
			formData: {}
		};
	},
	mounted() {
	},
	methods: {
		complete() {
			this.$refs.table.refresh();
		},
		search() {
			this.$refs.table.upData(this.param);
		},
		async status_item(e, row) {
			this.$SCM.status_item(this, this.$API.samplesdemo.status, row, row.row_status);
		},
		status_list(status) {
			this.$SCM.status_list(this, this.$API.samplesdemo.status, this.selection, status);
		},
		async delete_item(row) {
			this.$SCM.delete_item(this, this.$API.samplesdemo.delete, row);
		},
		delete_list() {
			this.$SCM.delete_list(this, this.$API.samplesdemo.delete, this.selection);
		},
		open_dialog(row) {
			this.$refs.edit.open(row);
		},
		selectionChange(selection) {
			this.selection = selection;
		},
		menuHandle(obj) {
			if (obj.command == "add") {
				this.open_dialog();
				return;
			}
			if (obj.command == "edit") {
				this.open_dialog(obj.row);
				return;
			}
			if (obj.command == "delete") {
				this.delete_item(obj.row);
				return;
			}
		}
	},
};
</script>